def train_one_epoch(model, loader, criterion, optimizer, device, writer, epoch, global_step):
    model.train()
    total_loss, total_acc, n = 0.0, 0.0, 0

    for batch in loader:
        images = batch['image'].to(device)
        labels = batch['label'].to(device)

        optimizer.zero_grad()
        logits = model(images)
        loss   = criterion(logits, labels)
        loss.backward()
        nn.utils.clip_grad_norm_(model.parameters(), max_norm=5.0)
        optimizer.step()

        bs = images.size(0)
        total_loss += loss.item() * bs
        total_acc  += compute_accuracy(logits.detach(), labels) * bs
        n          += bs

        writer.add_scalar('train/step_loss', loss.item(), global_step[0])
        global_step[0] += 1

    return {'loss': total_loss / n, 'acc': total_acc / n}


@torch.no_grad()
def evaluate(model, loader, criterion, device):
    model.eval()
    total_loss, total_acc, total_top3, n = 0.0, 0.0, 0.0, 0

    for batch in loader:
        images = batch['image'].to(device)
        labels = batch['label'].to(device)
        logits = model(images)
        loss   = criterion(logits, labels)

        bs = images.size(0)
        total_loss += loss.item() * bs
        total_acc  += compute_accuracy(logits, labels) * bs
        total_top3 += compute_top_k_accuracy(logits, labels, k=3) * bs
        n          += bs

    return {'loss': total_loss / n, 'acc': total_acc / n, 'top3': total_top3 / n}


