# ── Metrics & Utilities ───────────────────────────────────────────────────────

def compute_accuracy(logits, labels):
    return (logits.argmax(dim=1) == labels).float().mean().item()


def compute_top_k_accuracy(logits, labels, k=3):
    topk  = logits.topk(min(k, logits.size(1)), dim=1).indices
    match = topk.eq(labels.unsqueeze(1).expand_as(topk))
    return match.any(dim=1).float().mean().item()


def make_grid_of_augmented(images, n=8):
    """Denormalise a batch of images to TensorBoard grid."""
    mean = torch.tensor([0.485, 0.456, 0.406]).view(3, 1, 1)
    std  = torch.tensor([0.229, 0.224, 0.225]).view(3, 1, 1)
    imgs = (images[:n].cpu() * std + mean).clamp(0, 1)
    return torchvision.utils.make_grid(imgs, nrow=4)


