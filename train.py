# ── Model Architecture — CCNN (AugleNet) ──────────────────────────────────────

class ConvBnRelu(nn.Module):
    """Conv2d then BatchNorm2d then ReLU building block."""
    def __init__(self, in_ch: int, out_ch: int, kernel: int = 3, stride: int = 1):
        super().__init__()
        self.block = nn.Sequential(
            nn.Conv2d(in_ch, out_ch, kernel, stride=stride,
                      padding=kernel // 2, bias=False),
            nn.BatchNorm2d(out_ch),
            nn.ReLU(inplace=True),
        )

    def forward(self, x):
        return self.block(x)


class ResidualBlock(nn.Module):
    """
    Two-layer residual block.
    skip_proj=True  -> 1x1 conv to match channels (when in_ch != out_ch)
    skip_proj=False -> identity shortcut (requires in_ch == out_ch)
    pool=True       -> MaxPool2d(2,2) after residual add
    """
    def __init__(self, in_ch: int, out_ch: int,
                 skip_proj: bool = False, pool: bool = False):
        super().__init__()
        self.conv1    = ConvBnRelu(in_ch, out_ch, kernel=3)
        self.conv2    = nn.Sequential(
            nn.Conv2d(out_ch, out_ch, 3, padding=1, bias=False),
            nn.BatchNorm2d(out_ch),
        )
        self.shortcut = nn.Conv2d(in_ch, out_ch, 1, bias=False) if skip_proj else nn.Identity()
        self.relu     = nn.ReLU(inplace=True)
        self.pool     = nn.MaxPool2d(2, 2) if pool else nn.Identity()

    def forward(self, x):
        residual = self.shortcut(x)
        out      = self.conv1(x)
        out      = self.conv2(out)
        out      = self.relu(out + residual)
        return self.pool(out)


class CCNN(nn.Module):
    """
    Custom 4-stage CNN backbone.
    Input:  (B, 3, H, W)  -- H, W >= 32
    Output: (B, num_classes)  -- log-softmax
    """
    def __init__(self, num_classes: int, dropout: float = 0.4):
        super().__init__()
        self.stem   = ConvBnRelu(3, 32, kernel=3)
        self.stage1 = ResidualBlock(32,  64,  skip_proj=True,  pool=False)
        self.stage2 = ResidualBlock(64,  128, skip_proj=True,  pool=True)
        self.stage3 = ResidualBlock(128, 256, skip_proj=True,  pool=True)
        self.stage4 = ResidualBlock(256, 256, skip_proj=False, pool=True)
        self.gap     = nn.AdaptiveAvgPool2d(1)
        self.dropout = nn.Dropout(dropout)
        self.fc      = nn.Linear(256, num_classes)
        self.log_sm  = nn.LogSoftmax(dim=1)
        self._init_weights()

    def _init_weights(self):
        for m in self.modules():
            if isinstance(m, nn.Conv2d):
                nn.init.kaiming_normal_(m.weight, mode='fan_out', nonlinearity='relu')
            elif isinstance(m, nn.BatchNorm2d):
                nn.init.ones_(m.weight); nn.init.zeros_(m.bias)
            elif isinstance(m, nn.Linear):
                nn.init.xavier_uniform_(m.weight); nn.init.zeros_(m.bias)

    def forward(self, x):
        x = self.stem(x)
        x = self.stage1(x)
        x = self.stage2(x)
        x = self.stage3(x)
        x = self.stage4(x)
        x = self.gap(x).flatten(1)
        x = self.dropout(x)
        x = self.fc(x)
        return self.log_sm(x)

    def count_parameters(self):
        return sum(p.numel() for p in self.parameters() if p.requires_grad)


